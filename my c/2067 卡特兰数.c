#include<stdio.h>

__int64 b[38]={1,1};

int catalan(int i,int j)
{
    for(;i<38;i++)
    {
        b[i]=0;
        for(j=0;j<i;j++)
        {
            b[i]+=b[j]*b[i-j-1];
        }
    }
}

int main()
{
    int i=1,j=0;
    catalan(i,j);
    int t=1,n;
    while(~scanf("%d",&n)&&(n!=-1))
    {
        printf("%d %d %I64d\n",t,n,2*b[n]);
        t++;
    }
}
